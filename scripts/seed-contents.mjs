async function getMembers() {
  const res = await fetch(`${API_URL}/members`);
  if (!res.ok) throw new Error("メンバー取得に失敗");
  return res.json();
}

async function seedContents() {
  console.log("コンテンツデータを投入中...\n");

  // まずメンバーを取得
  let members;
  try {
    members = await getMembers();
    if (members.length === 0) {
      console.error("メンバーが存在しません。先に seed-members.mjs を実行してください。");
      return;
    }
    console.log(`${members.length}人のメンバーを取得しました\n`);
  } catch (err) {
    console.error("メンバー取得に失敗:", err.message);
    return;
  }

  // コンテンツを投入
  for (let i = 0; i < contentsData.length; i++) {
    const content = contentsData[i];
    // ランダムに著者を選択
    const author = members[i % members.length];

    try {
      const now = new Date();
      // 投稿日時をずらす（古い順に並ぶように）
      const createdAt = new Date(now.getTime() - (contentsData.length - i) * 24 * 60 * 60 * 1000);

      const res = await fetch(`${API_URL}/contents`, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          ...content,
          authorId: author.id,
          createdAt: createdAt.toISOString(),
          updatedAt: createdAt.toISOString(),
        }),
      });

      if (!res.ok) {
        throw new Error(`HTTP ${res.status}`);
      }

      const created = await res.json();
      const rankLabel = {
        guest: "非会員",
        bronze: "ブロンズ",
        silver: "シルバー",
        gold: "ゴールド",
      };
      console.log(`✓ [${rankLabel[created.requiredRank]}] ${created.title}`);
    } catch (err) {
      console.error(`✗ ${content.title} の作成に失敗:`, err.message);
    }
  }

  console.log("\n完了!");
}

